package application;

public class Multiplicacao {

	public static void main(String[] args) {

		// Declarando variaveis x e y, do tipo int.
		int x = 50;
		int y = 10;

		/*
		 * Declarando variavel result do tipo int, recebendo o valor da operação
		 * realizada.
		 */
		int result = (x * y);

		/* Imprimindo o valor da variavel */
		System.out.println("Valor de x = " + x);
		System.out.println("Valor de y = " + y);
		System.out.println("===============");

		/*
		 * Imprimindo o valor da variavel -> result concatenando com string.
		 */
		System.out.println("Resultado de ( x * y ) = " + result);

	}

}
